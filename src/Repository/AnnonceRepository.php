    /** @return array<int, array{urgence: string, total: string|int}> */
    public function countByUrgence(): array
    {
        return $this->createQueryBuilder('a')
            ->select('a.urgence AS urgence', 'COUNT(a.id) AS total')
            ->groupBy('a.urgence')
            ->getQuery()
            ->getResult();
